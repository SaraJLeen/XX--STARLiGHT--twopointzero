local function split_and_trim(sep, txt)
    local tbl = split(sep, txt)
    for k, v in pairs(tbl) do
        tbl[k] = v:gsub("^%s*",""):gsub("%s*$","")
    end
    return tbl
end

local function song_specific_dir(song)
    local parts = split_and_trim("/", song:GetSongDir())
    return string.lower(parts[#parts-1])
end

function get_subgroup_name(group,song)
	if not group then return ""; end
	if not song then return group; end
	if not subgroup_names[string.lower(group)] then return group; end
	local possibles = subgroup_names[string.lower(group)]
	local songd = song_specific_dir(song)
	if possibles[songd] then
		res = group.."\n"..possibles[songd]
	elseif possibles["default group"] then
		res = group.."\n"..possibles["default group"]
	else
		res = group
	end
	return res;
end